// using a for loop 
        
const rainfallData = [12, 15, 7, 22, 10, 5, 18, 9, 14, 6];

// Using a while loop to print each day's rainfall data
console.log("Rainfall in millimeters for each of the last 10 days:");
let i = 0;
while (i < rainfallData.length) {
    console.log("Day " + (i + 1) + ": " + rainfallData[i] + " mm");
    i++;
}









